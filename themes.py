def _normalize(s: str) -> str:
    """全角英数字→半角、大文字化（英字キーワード照合用）。"""
    out = []
    for ch in s:
        o = ord(ch)
        if 0xFF01 <= o <= 0xFF5E:          # 全角!-~ → 半角
            out.append(chr(o - 0xFEE0))
        else:
            out.append(ch)
    return ''.join(out).upper()


def _is_ascii(kw: str) -> bool:
    return all(ord(c) < 128 for c in kw)


def _kw_in(kw: str, raw: str, norm: str) -> bool:
    if _is_ascii(kw):
        ku = re.escape(kw.upper())
        # 前後がアルファベット/数字でない時だけ一致（英単語内の誤爆を防ぐ。日本語が隣は可）
        return re.search(r'(?<![A-Z0-9])' + ku + r'(?![A-Z0-9])', norm) is not None
    return kw in raw


def detect_themes(*texts, limit=4):
    """社名・業種・特色などのテキストからテーマ名のリストを返す。"""
    raw = ' '.join(t for t in texts if t)
    if not raw:
        return []
    norm = _normalize(raw)
    found = []
    for name, kws in THEME_DEFS:
        if any(_kw_in(kw, raw, norm) for kw in kws):
            found.append(name)
            if len(found) >= limit:
                break
    return found


def size_tag(market_cap_oku):
    """時価総額(億円)→ 規模タグ。"""
    if not market_cap_oku:
        return None
    v = market_cap_oku
    if v >= 10000:
        return '超大型'
    if v >= 3000:
        return '大型'
    if v >= 500:
        return '中型'
    if v >= 100:
        return '小型'
    return '超小型'
